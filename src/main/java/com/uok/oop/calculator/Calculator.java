    private void jButtonBackSpaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackSpaceActionPerformed
        int length = jTextField.getText().length();
        int number = jTextField.getText().length() - 1;
        String store;
        
        if (length > 0)
        {
            StringBuilder back = new StringBuilder(jTextField.getText());
            back.deleteCharAt(number);
            store = back.toString();
            jTextField.setText(store);
        }
    }//GEN-LAST:event_jButtonBackSpaceActionPerformed

