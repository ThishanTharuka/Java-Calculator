    public void arithmeticOperation()
    {
        switch(calculation)
        {
            case 1://Addition
                ans = num + Double.parseDouble(jTextField.getText());
                jTextField.setText(Double.toString(ans));
                break;
                
            case 2://Subtraction
                ans = num - Double.parseDouble(jTextField.getText());
                jTextField.setText(Double.toString(ans));
                break;
                
            case 3://Multiplication
                ans = num * Double.parseDouble(jTextField.getText());
                jTextField.setText(Double.toString(ans));
                break;
                
            case 4://Division
                ans = num / Double.parseDouble(jTextField.getText());
                jTextField.setText(Double.toString(ans));
                break;
                
            case 5://Percentage
                ans = num / 100;
                jTextField.setText(Double.toString(ans));
                break;
                
        }
    }
    
